import Foundation
import UIKit

extension UIColor {
    
    static var customDarkPurple: UIColor {
        return UIColor(red: 60/255, green: 25/255, blue: 110/255, alpha: 1.0)
    }
    
    // Optional: Add more named colors for easy reuse
    static var tabBarUnselected: UIColor {
        return .lightGray
    }
    
    static var tabBarSelected: UIColor {
        return .white
    }
    
}
